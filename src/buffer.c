void buffer_init(void)
{
  #define BUFFER_INIT_KEEP 1
  for (uint8_t i = 0; i < (TLC_N_CHANNELS / TLC_N_CHANNELS_PER_TLC - BUFFER_INIT_KEEP); i++)
    for (uint8_t rgb = 0; rgb < 3; rgb++)
      gg_buffer_gs[i * 3 + rgb] = 0xFF;//0x10 | (rgb + 1);
}

